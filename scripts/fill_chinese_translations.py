def fill_translations(po_file_path):
    """填充翻译文件"""
    with open(po_file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 替换每个msgstr ""
    lines = content.split('\n')
    result_lines = []
    i = 0
    while i < len(lines):
        line = lines[i]
        result_lines.append(line)
        
        # 检查是否是msgid行
        if line.startswith('msgid '):
            # 提取msgid的内容
            msgid_match = re.match(r'msgid "(.+)"', line)
            if msgid_match:
                msgid_text = msgid_match.group(1)
                # 查找对应的msgstr
                if i + 1 < len(lines) and lines[i + 1].startswith('msgstr ""'):
                    # 查找翻译
                    translation = TRANSLATIONS.get(msgid_text, "")
                    if translation:
                        result_lines.append(f'msgstr "{translation}"')
                        i += 1
                        continue
        
        # 处理多行msgid
        if line.startswith('msgid ""') and i + 1 < len(lines):
            # 多行msgid
            msgid_lines = [line]
            i += 1
            while i < len(lines) and (lines[i].startswith('"') or lines[i].strip() == ''):
                msgid_lines.append(lines[i])
                i += 1
            
            # 提取完整msgid文本
            msgid_text = ''
            for ml in msgid_lines[1:]:
                if ml.startswith('"'):
                    msgid_text += ml.strip('"').replace('\\n', '\n').replace('\\t', '\t')
            
            # 查找对应的msgstr
            if i < len(lines) and lines[i].startswith('msgstr ""'):
                translation = TRANSLATIONS.get(msgid_text.strip(), "")
                if translation:
                    result_lines.extend(msgid_lines)
                    result_lines.append(f'msgstr "{translation}"')
                    i += 1
                    continue
                else:
                    result_lines.extend(msgid_lines)
                    result_lines.append(lines[i])
                    i += 1
                    continue
            else:
                result_lines.extend(msgid_lines)
                continue
        
        i += 1
    
    # 更新文件头
    result_content = '\n'.join(result_lines)
    result_content = result_content.replace('Language: \n', 'Language: zh_Hans\n')
    result_content = result_content.replace('Plural-Forms: nplurals=1; plural=0;\n', 'Plural-Forms: nplurals=1; plural=0;\n')
    
    with open(po_file_path, 'w', encoding='utf-8') as f:
        f.write(result_content)
    
    print(f"翻译文件已更新: {po_file_path}")

